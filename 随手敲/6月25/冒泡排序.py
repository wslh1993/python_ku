l=[23,24,45,123,66,4,4,8,7,78]
for i in range(len(l)-1):
    for j in range(len(l)-1-i):#-i表示前面大的数不用再比较一遍依次 9 8 7 6 5 4 3 2 
        if l[j]>l[j+1]:
            l[j],l[j+1]=l[j+1],l[j]
print(l)

